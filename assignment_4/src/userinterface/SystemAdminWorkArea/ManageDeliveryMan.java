    public void displayDeliveryTable(){
        DefaultTableModel tablemodel = (DefaultTableModel) tblDelivery.getModel();
        
        tablemodel.setRowCount(0);
        for(UserAccount userAccount : ecosystem.getUserAccountDirectory().getUserAccountList()){
            if(userAccount.getRole().getClass().getName()=="Business.Role.DeliverManRole"){
                Object [] row = new Object[3];
                row[0] = userAccount.getName();
                row[1] = userAccount.getUsername();
                row[2] = userAccount.getPassword();
                
                tablemodel.addRow(row);
            }
        }
    }

