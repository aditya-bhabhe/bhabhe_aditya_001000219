    private void btnDeliveryUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeliveryUpdateActionPerformed
        // TODO add your handling code here:
        int focusRow = tblDelivery.getSelectedRow();

        if(focusRow >= 0){
            String name = (String) tblDelivery.getValueAt(focusRow, 0);
            String username= (String) tblDelivery.getValueAt(focusRow, 1);
            String password= (String) tblDelivery.getValueAt(focusRow, 2);
            userAccount = ecosystem.getUserAccountDirectory().authenticateUser(username, password);

            txtCustomerName.setText(name+"");
            txtCustomerUserName.setText(username+"");
            txtCustomerPassword.setText(password+"");

            JOptionPane.showMessageDialog(null,"Please press save button to save the Delivery Man information after updating the text field");

        }
        else {
            JOptionPane.showMessageDialog(null,"Please select a restaurant to update a Delivery Man profile");
        }
    }//GEN-LAST:event_btnDeliveryUpdateActionPerformed

    private void btnDeliveryDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeliveryDeleteActionPerformed
        // TODO add your handling code here:
        int focusRow = tblDelivery.getSelectedRow();
        if(focusRow >= 0){
            int focusButton = JOptionPane.YES_NO_OPTION;
            int focusResult = JOptionPane.showConfirmDialog(null, "Confirm delete?","Warning",focusButton);
            if(focusResult == JOptionPane.YES_OPTION){
                String username= (String) tblDelivery.getValueAt(focusRow, 1);
                String password= (String) tblDelivery.getValueAt(focusRow, 2);
                UserAccount user=ecosystem.getUserAccountDirectory().authenticateUser(username, password);

                ecosystem.getUserAccountDirectory().deleteUserAccount(userAccount);
                ecosystem.getCustomerDirectory().deleteCustomer(userAccount.getUsername());
                displayDeliveryTable();
            }else{
                JOptionPane.showMessageDialog(null, "Please select a delivery man profile to delete the restaurant account");
            }
        }
    }//GEN-LAST:event_btnDeliveryDeleteActionPerformed

